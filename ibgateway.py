"""
ibgateway — IBKR Gateway Lifecycle Manager
===================================================
Manages IB Gateway start/stop/status, credentials, config generation,
and script installation.

Usage (via run_service.py or session_manager.py):
    python run_service.py --mode start
    python run_service.py --mode stop
    python run_service.py --mode status
"""

